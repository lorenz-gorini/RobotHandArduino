from lib.GenericProcess import GenericProcess


class VisualizeSpectraProcess(GenericProcess):
    def __init__(self, spectrum_batches: mp.Queue, visualized_batches: mp.Queue):
        super().__init__()
        self.stored_spectrum_batches = spectrum_batches
        self.visualized_spectrum_batches = visualized_batches


    def run(self):
        while not self.exit.is_set():
            self._visualize_spectra()
        print("You exited!")


    def _visualize_spectra(self):

        fig = plt.figure()
        ax = plt.axis([-1,1, -50, 50])
        sub_plot = fig.add_subplot(1, 1, 1)

        anim_graph = AnimatedGraph(sub_plot, self.stored_spectrum_batches,
                      self.visualized_spectrum_batches)
        anim_graph.animate_spectra()
        self.shutdown()


class AnimatedGraph():
    def __init__(self, sub_plot, spectrum_batches: mp.Queue, visualized_batches: mp.Queue):
        self.spectrum_batches = spectrum_batches
        self.visualized_batches = visualized_batches
        self.sub_plot = sub_plot

        self.freq_signal_real_plot, self.freq_signal_imag_plot = self.sub_plot.plot([0], [0], [0], [0])
        plt.ion()
        plt.show()
        self.animate_spectra()
        # self.anim_graph = animation.FuncAnimation(fig, self.animate, interval=global_values.animation_interval)

    def animate_spectra(self):

        while not global_values.total_shutdown.is_set():
            if self.spectrum_batches.empty():
                continue
            else:
                freq_batch, data_to_visualize = self.spectrum_batches.get(timeout=5)
                print("\"",freq_batch,"\"")
                print('\n\n\n',data_to_visualize)
                if freq_batch is None and data_to_visualize is None:
                    # Waiting a bit for visualization of last graph and then closing
                    print("Visualizing plot")
                    time.sleep(5)
                    print("Closing plot")
                    plt.close()
                    break
                else:
                    self._update_sub_plot(freq_batch, data_to_visualize)
                    self.visualized_batches.put((freq_batch, data_to_visualize))



        # After visualizing the animation, the plot is closed
        print("Plot closed")

    def _update_sub_plot(self, freq_batch, data_to_visualize):

        self.freq_signal_real_plot = self.sub_plot.plot(freq_batch, data_to_visualize.real)
        self.freq_signal_imag_plot = self.sub_plot.plot(freq_batch, data_to_visualize.imag)
        plt.draw()


